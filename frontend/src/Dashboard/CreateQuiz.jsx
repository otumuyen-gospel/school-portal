import Box from "@mui/material/Box";
import Button from "@mui/material/Button";
import Checkbox from "@mui/material/Checkbox";
import CircularProgress from "@mui/material/CircularProgress";
import FormControl from "@mui/material/FormControl";
import Grid from "@mui/material/Grid";
import InputLabel from "@mui/material/InputLabel";
import MenuItem from "@mui/material/MenuItem";
import Select from "@mui/material/Select";
import TextField from "@mui/material/TextField";
import Typography from "@mui/material/Typography";
import { AdapterDayjs } from "@mui/x-date-pickers/AdapterDayjs";
import { DatePicker } from "@mui/x-date-pickers/DatePicker";
import { LocalizationProvider } from "@mui/x-date-pickers/LocalizationProvider";
import dayjs from "dayjs";
import { EditorState, convertToRaw } from 'draft-js';
import { useEffect, useState } from "react";
import { Editor } from "react-draft-wysiwyg";
import "react-draft-wysiwyg/dist/react-draft-wysiwyg.css";
import axiosInstance from "../Util/ApiRefresher";
import Layout from "../Util/Layout";
import MessageDialogForm from "../Util/MessageDialogForm";


function CreateQuiz(){
  const [authUser] = useState(JSON.parse(localStorage.getItem('auth')));
  const [isLoading, setIsLoading] = useState(false);
  const [isDisabled, setIsDisabled] = useState(false);
  const [openMsgBox, setOpenMsgBox] = useState(false);
  const [msg, setMsg] = useState("");
  const [subjectList, setSubjectList] = useState([]);
  const [detailsMsg, setDetailsMsg] = useState("");
  const [form, setForm] = useState({
    option1:"",
    option2:"",
    option3:"",
    answer:"",
    subjectId:"",
    classId:"",
    endDate:dayjs(),
    startDate:dayjs(),
    setAsQuiz:false,
  });
  const [editorState, setEditorState] = useState(EditorState.createEmpty());
    const onEditorStateChange = (newEditorState) => {
        setEditorState(newEditorState);
    };
    const convertForDatabase = ()=>{
      return JSON.stringify(convertToRaw(editorState.getCurrentContent()));
    }
  const handleOpenMsgBox = ()=>{
    setOpenMsgBox(true);
  }
  const handleCloseMsgBox = ()=>{
    setOpenMsgBox(false);
  }
  
  useEffect(()=>{
    //fetch all paginated subject data by recursively calling page by page
    const listSubjects = async(url)=>{
      try{
         const response = await axiosInstance.get(url)
          const data = response.data.results;
          const nextPage = response.data.next;
          if(nextPage){
            return data.concat(await listSubjects(nextPage));
          }else{
            return data;
          }
      }catch(error){
         setMsg(`Oops! sorry can't load subject List`);
         throw error; //rethrow consequent error
     }
    }
    
    if(authUser){
       const url = "subjects/class-subject-list/"+
       authUser['user'].classId+"/";
       listSubjects(url).then(allData=>{
         setSubjectList(allData)
      }).catch((error)=>{
         setMsg(`Oops! sorry can't load subject List`);
         handleOpenMsgBox();
      })
    }
   
  },[authUser])

 const handleSubmit = (event)=>{
    event.preventDefault();
    event.target.checkValidity();
    const dbData  = convertForDatabase();
    if(!dbData){
      setDetailsMsg("please kindly enter the Question");
      return;
    }else{
      setDetailsMsg("");
    }
    setIsLoading(true);
    const data ={
    question:dbData,
    option1:form.option1,
    option2:form.option2,
    option3:form.option3,
    answer:form.answer,
    subjectId:form.subjectId,
    classId:authUser ? authUser['user'].classId :"",
    endDate:dayjs(form.endDate).format("YYYY-MM-DD"),
    startDate:dayjs(form.startDate).format("YYYY-MM-DD"),
    setAsQuiz:form.setAsQuiz,};
    axiosInstance.post("quizzes/create-quiz/",
          data).then((res) => {
            setIsLoading(false)
            setIsDisabled(false)  //re-enable button
            setMsg("quiz created successfully");
            handleOpenMsgBox();
    }).catch((err) => {
            setIsLoading(false)
            setIsDisabled(false)  //re-enable button
            if (err) {
              setMsg(JSON.stringify(err.response.data));
                 handleOpenMsgBox();
            }
    })
        
  }
  
  return (
    <div style={{backgroundColor:"#FFF"}}>
      <Layout title="Add Quiz">
        <Box 
       sx={{
          minHeight:"100vh",
          marginTop:"10px",
        }}
        >
        <Typography component="h1" variant="h6">Add Quiz</Typography>
        <Box component="form" onSubmit={handleSubmit} sx={{
           width:{xs:"100%",}}}>
            <Typography marginTop={5} style={{color:"royalblue"}}>
              Quiz details</Typography>
             <Box boxShadow={1} marginBottom={5} borderTop="5px solid royalblue"
                        marginTop={5} padding="10px 30px">

                <Grid container spacing={1}>
                   <Grid item size={{xs:12, sm:12, md:12}}>
                       <span>{detailsMsg ? detailsMsg : "Quiz Question"}</span>
                        <Editor
                          minHeight="150px"
                          editorState={editorState}
                          onEditorStateChange={onEditorStateChange}
                        />
                   </Grid>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <TextField
                        sx={{
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}
                        fullWidth
                        margin="normal"
                        required
                        id="option1"
                        label="option1"
                        type="text"
                        value={form.option1}
                        onChange={(e) => setForm({ ...form,
                              option1: e.target.value })}
                        name="option1"
                 
                      />
                   </Grid>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <TextField
                        sx={{
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}
                        fullWidth
                        margin="normal"
                        required
                        id="option2"
                        label="option2"
                        type="text"
                        value={form.option2}
                        onChange={(e) => setForm({ ...form,
                              option2: e.target.value })}
                        name="option2"
                 
                      />
                   </Grid>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <TextField
                        sx={{
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}
                        fullWidth
                        margin="normal"
                        required
                        id="option3"
                        label="option3"
                        type="text"
                        value={form.option3}
                        onChange={(e) => setForm({ ...form,
                              option3: e.target.value })}
                        name="option3"
                 
                      />
                   </Grid>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <TextField
                        sx={{
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}
                        fullWidth
                        margin="normal"
                        required
                        id="answer"
                        label="answer"
                        type="text"
                        value={form.answer}
                        onChange={(e) => setForm({ ...form,
                              answer: e.target.value })}
                        name="answer"
                 
                      />
                   </Grid>
                </Grid>
              </Box>


              <Typography marginTop={5} style={{color:"royalblue"}}>
              Other details</Typography>
             <Box boxShadow={1} marginBottom={5} borderTop="5px solid royalblue"
                        marginTop={5} padding="10px 30px">

                <Grid container spacing={1}>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <Checkbox
                        sx={{
                           display:"inline",
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}
                        fullWidth
                        margin="normal"
                        required
                        id="setAsQuiz"
                        label="setAsQuiz"
                        checked={form.setAsQuiz}
                        onChange={(e) =>{setForm({ ...form,
                           setAsQuiz: e.target.checked })}}
                       name="setAsQuiz"
                 
                     />
                     <Typography style={{display:"inline"}}>
                        Set and Activate Quiz to Run Immediately after creation
                     </Typography>
                   </Grid>
                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <FormControl 
                      required  
                      fullWidth
                      sx={{
                            margin:"16px 0px",
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}>
                      <LocalizationProvider dateAdapter={AdapterDayjs}>
                      <DatePicker
                       id="startDate"
                       label="startDate"
                       value={dayjs(form.startDate)}
                       format="YYYY-MM-DD"
                       onChange={(e) =>setForm({ ...form, startDate: e })}
                       name="startDate"
                 
                       /></LocalizationProvider>
                     </FormControl>
                   </Grid>

                    <Grid item size={{xs:12, sm:6, md:6}}>
                      <FormControl 
                      required  
                      fullWidth
                      sx={{
                            margin:"16px 0px",
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}>
                      <LocalizationProvider dateAdapter={AdapterDayjs}>
                      <DatePicker
                       id="endDate"
                       label="endDate"
                       value={dayjs(form.endDate)}
                       format="YYYY-MM-DD"
                       onChange={(e) =>setForm({ ...form, endDate: e })}
                       name="endDate"
                 
                       /></LocalizationProvider>
                     </FormControl>
                   </Grid>

                   <Grid item size={{xs:12, sm:6, md:6}}>
                      <FormControl 
                      required  
                      fullWidth
                      sx={{
                            margin:"16px 0px",
                           '& .MuiInputBase-root':{
                            height:"50px",
                            borderRadius:"10px",
                         },
                         '& .MuiOutlinedInput-input':{
                         height:"50px",
                         paddingTop:0,
                         paddingBottom:0,
                         },
                        }}>
                       <InputLabel id="subject-label">{form.subjectId || "subject"}</InputLabel>
                       <Select
                        fullWidth
                        margin="normal"
                        labelId="subject-label"
                        id="subjectId"
                        name="subjectId"
                        value={form.subjectId}
                        label="Subject"
                        onChange={(e) => setForm({ ...form,
                           subjectId: e.target.value })}
                        >
                        <MenuItem value="">None</MenuItem>
                          {
                            subjectList.map(list=>(
                              <MenuItem key={list.id}
                               value={list.id}>{list.subjectCode}</MenuItem>
                            ))
                        
                          }
                        </Select>
                     </FormControl>
                   </Grid>
                </Grid>
              </Box>
              
              <div style={{textAlign:"center"}}>
              <Button
              type="submit"
              fullWidth
              variant="contained"
              disabled={isDisabled}
              sx={{mt: 2, mb: 2, height:"50px", width:"150px",
              borderRadius:"10px" }}>Upload</Button></div>

              <div className="loaderContainer" marginBottom={10}>
                     {isLoading && <CircularProgress />}
               </div>
          </Box>
      </Box>

        <MessageDialogForm open={openMsgBox} 
        onClose={handleCloseMsgBox} 
        formContent={<Typography>{msg}</Typography>}
        title="Message Dialog"
        />
      </Layout>
    </div>
    
);

}


export default CreateQuiz;